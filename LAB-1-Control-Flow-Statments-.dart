void main() {
  var integer = '7';

  switch (integer) {
    case '1':
      print('Monday');
      break;
    case '2':
      print('Tuesday');
      break;
    case '3':
      print('Wednsday');
      break;
    case '4':
      print('Thursday');
      break;
    case '5':
      print('Friday');
      break;
    case '6':
      print('Saturday');
      break;
    case '7':
      print('Sunday');
      break;
    default:
      print('No days of the week which corresponds to the number!');
  }
}
